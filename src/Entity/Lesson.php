<?php

namespace App\Entity;

use App\Repository\LessonRepository;
use Doctrine\Common\Collections\ArrayCollection;
use Doctrine\Common\Collections\Collection;
use Doctrine\ORM\Mapping as ORM;

#[ORM\Entity(repositoryClass: LessonRepository::class)]
class Lesson
{
    #[ORM\Id]
    #[ORM\GeneratedValue]
    #[ORM\Column(type: 'integer')]
    private $id;

    #[ORM\ManyToOne(targetEntity: Day::class, inversedBy: 'lessons')]
    private $day;

    #[ORM\ManyToOne(targetEntity: Hour::class, inversedBy: 'lessons')]
    private $hour;

    #[ORM\ManyToOne(targetEntity: Teacher::class, inversedBy: 'lessons')]
    private $teacher;

    #[ORM\ManyToOne(targetEntity: Instrument::class, inversedBy: 'lessons')]
    private $instrument;

    #[ORM\ManyToMany(targetEntity: Member::class, mappedBy: 'lesson')]
    private $members;

    public function __construct()
    {
        $this->members = new ArrayCollection();
    }

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getDay(): ?Day
    {
        return $this->day;
    }

    public function setDay(?Day $day): self
    {
        $this->day = $day;

        return $this;
    }

    public function getHour(): ?Hour
    {
        return $this->hour;
    }

    public function setHour(?Hour $hour): self
    {
        $this->hour = $hour;

        return $this;
    }

    public function getTeacher(): ?Teacher
    {
        return $this->teacher;
    }

    public function setTeacher(?Teacher $teacher): self
    {
        $this->teacher = $teacher;

        return $this;
    }

    public function getInstrument(): ?Instrument
    {
        return $this->instrument;
    }

    public function setInstrument(?Instrument $instrument): self
    {
        $this->instrument = $instrument;

        return $this;
    }

    /**
     * @return Collection<int, Member>
     */
    public function getMembers(): Collection
    {
        return $this->members;
    }

    public function addMember(Member $member): self
    {
        if (!$this->members->contains($member)) {
            $this->members[] = $member;
            $member->addLesson($this);
        }

        return $this;
    }

    public function removeMember(Member $member): self
    {
        if ($this->members->removeElement($member)) {
            $member->removeLesson($this);
        }

        return $this;
    }
}
